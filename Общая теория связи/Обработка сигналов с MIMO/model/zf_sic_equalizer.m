function detected_symbols = zf_sic_equalizer(y, H, Nt, Nr)
    % y: Принятый вектор-столбец (Nr x 1)
    % H: Матрица канала (Nr x Nt)
    % Nt, Nr: Количество передающих и приемных антенн

    detected_symbols = zeros(Nt, 1);

    y_current = y;
    H_current = H; % Копия матрицы канала для модификации

    % Порядок обнаружения фиксирован (например, 1, 2, ..., Nt)
    for i = 1:Nt
        % Вычисление ZF-фильтра для текущей эффективной системы
        % H_current может иметь нули в столбцах, если символы вычтены

        % Создаем "эффективную" H_eff, удаляя уже обработанные столбцы
        % В данном случае, проще использовать оригинальную H и управлять индексами

        % Для ZF-детектора нам нужна инверсия H'*H для текущих оставшихся столбцов
        % Если H_current становится сингулярной, pinv() более устойчив

        % В SIC мы последовательно обнаруживаем один поток за другим.
        % Поэтому для i-го потока, мы рассматриваем H_current как (Nr x (Nt-i+1))

        % Находим псевдообратную матрицу для H_current (если какие-то столбцы обнулены)
        W_zf = pinv(H_current); % (Nt x Nr)

        % ZF-фильтр для текущего (i-го) потока: i-я строка W_zf
        w_i_zf = W_zf(i, :)'; % (Nr x 1)

        % Обнаружение текущего символа
        s_i_hat = w_i_zf' * y_current;

        % Квантование к ближайшему символу QPSK
        s_i_demod = qamdemod(s_i_hat, 4, 'OutputDataType', 'double', 'UnitAveragePower', true);
        s_i_quant = qammod(s_i_demod, 4, 'InputType', 'bit', 'UnitAveragePower', true);

        detected_symbols(i) = s_i_quant;
  % Вычитание помехи от обнаруженного символа
        y_current = y_current - H(:,i) * s_i_quant;
        % Важно: вычитаем из оригинальной матрицы H, чтобы не портить H_current для pinv
        % Это ключевое отличие от прямого удаления столбцов.
        % Чтобы H_current правильно отражала оставшиеся потоки для pinv,
        % нужно обнулять столбец H_current
        H_current(:, i) = 0; % Исключаем вклад этого символа для следующих итераций ZF
    end
end
